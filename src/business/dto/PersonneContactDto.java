package business.dto;

public interface PersonneContactDto {

  /**
   * Retourne l'id de la personne.
   * 
   * @return the idPersonne
   */
  int getIdPersonne();


  /**
   * Modifie l'id de la personne.
   * 
   * @param idPersonne the idPersonne to set
   */
  void setIdPersonne(int idPersonne);


  /**
   * Retourne le nom de la personne.
   * 
   * @return the nom
   */
  String getNom();


  /**
   * Modifie le nom de la personne.
   * 
   * @param nom the nom to set
   */
  void setNom(String nom);


  /**
   * Retourne le prénom de la personne.
   * 
   * @return the prenom
   */
  String getPrenom();


  /**
   * Modifie le prénom de la personne.
   * 
   * @param prenom the prenom to set
   */
  void setPrenom(String prenom);


  /**
   * Retourne le mail de la personne.
   * 
   * @return the email
   */
  String getEmail();


  /**
   * Modifie le mail de la personne.
   * 
   * @param email the email to set
   */
  void setEmail(String email);

  /**
   * Retourne le numéro de téléphone de la personne.
   * 
   * @return the telephone
   */
  String getTelephone();


  /**
   * Modifie le numéro de téléphone de la personne.
   * 
   * @param telephone the telephone to set
   */
  void setTelephone(String telephone);

  /**
   * Retourne le numéro de version.
   * 
   * @return the numVersion
   */
  int getNumVersion();

  /**
   * Modifie le numéro de version.
   * 
   * @param numVersion the numVersion to set
   */
  void setNumVersion(int numVersion);

  /**
   * Retourne true si la personne de contact est active.
   * 
   * @return the actif
   */
  boolean isActif();

  /**
   * Modifie l'activité de la personne de contact.
   * 
   * @param actif the actif to set
   */
  void setActif(boolean actif);

  /**
   * Retourne l'id de l'entreprise de la personne de contact.
   * 
   * @return the idEntreprise
   */
  int getIdEntreprise();

  /**
   * Modifie l'id de l'entreprise de la personne de contact.
   * 
   * @param idEntreprise the idEntreprise to set
   */
  void setIdEntreprise(int idEntreprise);

  /**
   * Retourne le nom de l'entreprise de la personne de contact.
   * 
   * @return the nomEntreprise;
   */
  String getNomEntreprise();

  /**
   * Modifie le nom de l'entreprise de la personne de contact.
   * 
   * @param nomEntreprise the nomEntreprise to set
   */
  void setNomEntreprise(String nomEntreprise);

  /**
   * Renvoie true si la personne de contact est annoncee comme presente.
   * 
   * @return the present.
   */
  boolean isPresent();

  /**
   * Modifie la presence de la personne de contact.
   *
   * @param present the present to set.
   */
  void setPresent(boolean present);
}
